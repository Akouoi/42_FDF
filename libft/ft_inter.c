#include <unistd.h>

int	ft_inter(char *s1, char*s2, int n)
{
	int		j;
	int		i;
	char	s[256];

	i = 0;
	j = 0;
	while (s1[j] && j < n)
		s[(int)s1[j++]] = 1;
	j = 0;
	while (s2[j] && j < n)
		s[(int)s2[j++]] += 1;
	j = 0;
	while (s1[j] && j < n)
	{
		if (s[(int)s1[j]] == 2)
		{
			i++;
			s[(int)s1[j]] = 3;
		}
		j++;
	}
	return (i);
}
